#!/usr/bin/env python3
"""
Generate [[example]] entries for Cargo.toml - only compilable examples.

Usage:
    python generate_cargo_examples.py > /tmp/examples.txt

Then copy output to Cargo.toml under the examples section.
"""

import os
from pathlib import Path

# Test directories that are NOT standalone compilable
SKIP_DIRS = {
    # Uses spec types (not compilable standalone - would need trait impls)
    '016_basic_spec',
    '017_spec',
    '031_spec',
    # Uses delegation (requires trait impl generation)
    '018_delegation',
    '019_multi_delegation',
    '020_delegation_params',
    '032_delegation',
    '033_multi_delegation',
    '034_delegation_params',
    # Uses inheritance
    '035_inheritance',
    # Uses storage types that need external crate
    '114_storage_module',
    '115_storage_usage',
    '116_plan055_auto_storage',
    # No expected.rs files
    '013_union',
    '111_generic_type_alias',
    '112_generic_specs',
    '113_generic_spec_ext',
}

def main():
    script_dir = Path(__file__).parent

    print("# Auto-generated example targets for a2r tests")
    print("# Run: cargo run -p auto-lang --example 000_hello")
    print("# Generated by: python test/a2r/generate_cargo_examples.py")
    print()

    count = 0
    for test_dir in sorted(script_dir.iterdir()):
        if not test_dir.is_dir():
            continue

        # Skip non-test directories
        if test_dir.name.startswith('.'):
            continue

        # Skip known non-compilable tests
        if test_dir.name in SKIP_DIRS:
            continue

        # Find .expected.rs file
        expected_files = list(test_dir.glob("*.expected.rs"))
        if not expected_files:
            continue

        # Get example name from directory (e.g., "000_hello")
        example_name = test_dir.name
        rs_file = expected_files[0].name

        print(f"[[example]]")
        print(f'name = "{example_name}"')
        print(f'path = "test/a2r/{example_name}/{rs_file}"')
        print()
        count += 1

    print(f"# Total: {count} examples")

if __name__ == "__main__":
    main()
